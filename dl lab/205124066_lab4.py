import tensorflow as tf
from tensorflow import keras
from tensorflow.keras import layers
img_size = (224 , 224)

train_ds = tf.keras.preprocessing.image_dataset_from_directory("drive/MyDrive/tomoto_leaf/train", image_size = img_size , batch_size = 32 , label_mode = "binary")



import matplotlib.pyplot as plt

