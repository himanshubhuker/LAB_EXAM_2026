for images, labels in train_ds.take(1):
    print("Image shape:", images[0].shape)
    print("Label type:", labels[0].dtype)
    print("Label value:", labels[0].numpy())

val_ds = tf.keras.preprocessing.image_dataset_from_directory("drive/MyDrive/tomoto_leaf/valid", image_size = img_size , batch_size = 32 , label_mode = "binary")

import matplotlib.pyplot as plt

plt.figure(figsize=(10, 10))
for images, labels in val_ds.take(1):
    for i in range(9):
        ax = plt.subplot(3, 3, i + 1)
        plt.imshow(images[i].numpy().astype("uint8"))
        plt.title(int(labels[i]))
        plt.axis("off")

test_ds = tf.keras.preprocessing.image_dataset_from_directory("drive/MyDrive/tomoto_leaf/test", image_size = img_size , batch_size = 32 , label_mode = "binary")

import matplotlib.pyplot as plt

plt.figure(figsize=(10, 10))
for images, labels in test_ds.take(1):
    for i in range(9):
        ax = plt.subplot(3, 3, i + 1)
        plt.imshow(images[i].numpy().astype("uint8"))
        plt.title(int(labels[i]))
        plt.axis("off")

data_augmentation = keras.Sequential([
    layers.RandomFlip("horizontal"),
    layers.RandomRotation(0.1),
])

base_model = keras.applications.DenseNet121(
    include_top=False,
    weights="imagenet",
    input_shape=img_size + (3,)
)

base_model.trainable = False
inputs = keras.Input(shape=img_size + (3,))
x = data_augmentation(inputs)
x = keras.applications.densenet.preprocess_input(x)
x = base_model(x, training=False)
x = layers.GlobalAveragePooling2D()(x)
x = layers.Dense(256, activation="relu")(x)
x = layers.Dropout(0.3)(x)
outputs = layers.Dense(1, activation="sigmoid")(x)
model = keras.Model(inputs, outputs)
# class_weight or pos_weight equivalent: use class_weight in model.fit
model.compile(
    optimizer=keras.optimizers.Adam(1e-4),
    loss="binary_crossentropy",
    metrics=["accuracy", keras.metrics.AUC(name="auc")]
)
# Example class weights (compute from dataset first)
class_weight = {0: 1.0, 1: 2.0}
model.fit(train_ds, epochs=10, validation_data=val_ds, class_weight=class_weight)
test_metrics = model.evaluate(test_ds)
print("Test:", test_metrics)

print(f"Test Accuracy: {test_metrics[1]:.4f}")



